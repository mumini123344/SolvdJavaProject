package transportation;



public class CarStation {
    private String carStationName;
    private String carStationLocation;
    private int carStationCapacity;
    private String sponsor;

    public String getCarStationName() {
        return carStationName;
    }

    public void setCarStationName(String carStationName) {
        this.carStationName = carStationName;
    }

    public String getCarStationLocation() {
        return carStationLocation;
    }

    public void setCarStationLocation(String carStationLocation) {
        this.carStationLocation = carStationLocation;
    }

    public int getCarStationCapacity() {
        return carStationCapacity;
    }

    public void setCarStationCapacity(int carStationCapacity) {
        this.carStationCapacity = carStationCapacity;
    }

    public String getSponsor() {
        return sponsor;
    }

    public void setSponsor(String sponsor) {
        this.sponsor = sponsor;
    }
}

